// 1. Apa itu Encapsulation? Mengapa kita membutuhkannya?
// Jawab: Encapsulation adalah cara untuk membatasi akses langsung ke properti atau method dari sebuah objek. ini dibutuhkan apabila kita ingin membatasi akses variabel tertentu agar tidak mudah dirubah

// 2. Apa itu Abstraction? Mengapa kita membutuhkannya?
// Jawab: Abstaraction merupakan sebuah teknik untuk menyembunyikan detail tertentu dari sebuah objek/method dan hanya menampilkan fungsionalitas atau fitur penting dari objek tersebut.

// 3. Apa itu Inheritance? Mengapa kita membutuhkannya?
// Jawab: Inheritance merupakan sebuah proses dimana sebuah class mewariskan property dan methodnya ke class lain atau childnya. kita membutuhannya karena agar tidak mengulangi atribut yang sama.

// 4. Apa itu Polymorpishm? Mengapa kita membutuhkannya?
// Jawab: Polymorpishm merupakan kemampuan dari suatu objek untuk memiliki banyak bentuk. kita membutuhkan ini karena untuk membuat bentuk lain atribut sama dengan method yang berbeda
